public class PunktLast {
    //Attribute
    private double ort;
    private double kraft;

    //Konstruktor
    public PunktLast(double o,double k){
        ort=o;
        kraft=k;
    }

    //Getter und Setter
    public double getOrt() {
        return ort;
    }

    public void setOrt(double ort) {
        this.ort = ort;
    }

    public double getKraft() {
        return kraft;
    }

    public void setKraft(double kraft) {
        this.kraft = kraft;
    }
}
